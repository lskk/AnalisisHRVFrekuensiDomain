% --- Executes on button press in btInput.
function btInput_Callback(hObject, eventdata, handles)
% hObject    handle to btInput (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

[filename,pathname] = uigetfile( ...
{'*.txt',  'Select Heart Rate Data File (*.txt)'; ...
'*.txt',  'Text File (*.txt)'}, ...
   'Select Testing File :');

filestr=strcat(pathname,filename);
exts=filename(length(filename)-2:length(filename));

if(isempty(filestr))
    msgbox('Please select heart rate data file first','Information');
    return;
end

stress_train = fullfile(pwd,'\svm_stress_train.mat')
if(~exist(stress_train))
   msgbox('Please train data file first','Informasi');
   return; 
end

load(stress_train);
svm_struct_stress = svm_struct_stress;

flu_train = fullfile(pwd,'\svm_flu_train.mat')
if(~exist(flu_train))
   msgbox('Please train data file first','Informasi');
   return; 
end

load(flu_train);
svm_struct_flu = svm_struct_flu;

klasifikasi_svm(svm_struct_stress, svm_struct_flu,filestr, handles);



